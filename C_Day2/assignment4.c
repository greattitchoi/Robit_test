#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

// ============ 함수: 부분 수열의 중앙값 확인 ============
// 매개변수: arr[] - 부분 수열, size - 부분 수열의 길이, b - 찾는 중앙값
// 반환값: 1 (중앙값이 b와 일치), 0 (일치하지 않음)
int distin (int arr[],int size,int b){
    // ============ 1단계: 선택 정렬으로 배열 정렬 ============
    for (int i=size-1;i>=0;i--){
        int max=arr[0];       // 최댓값 초기화
        int max_index=0;      // 최댓값의 위치 초기화
        
        // 0부터 i까지의 범위에서 최댓값 찾기
        for (int j=0;j<=i;j++){
            if(max<=arr[j]){
                max=arr[j];           // 최댓값 갱신
                max_index=j;          // 최댓값의 위치 갱신
            }
        }
        
        // 찾은 최댓값을 배열의 i번째 위치로 이동
        arr[max_index]=arr[i];  // 기존 i번째 값을 최댓값 위치에 저장
        arr[i]=max;             // 최댓값을 i번째 위치에 저장
    }
   
    // ============ 2단계: 중앙값 확인 ============
    // 정렬 후, 배열의 중앙에 있는 요소가 B인지 확인
    // size가 홀수이므로 size/2가 정확한 중앙 위치
    if (arr[size/2]==b){
        return 1;  // 중앙값이 B와 일치하면 1 반환
    }
    else return 0;  // 일치하지 않으면 0 반환
}

int main (void){
    int N,B;      // N: 수열의 길이, B: 찾는 중앙값
    int num=0;    // 조건을 만족하는 부분 수열의 개수
    
    printf("N: ");
    scanf("%d",&N);
    printf("B: ");
    scanf("%d",&B);
    
    int arr[100]={0,};       // 원본 수열을 저장할 배열
    int test_arr[100]={0,};  // 부분 수열을 저장할 임시 배열
    
    // ============ 입력받은 수열을 arr에 저장 ============
    for (int i=0;i<N;i++){
        scanf("%d",&arr[i]);
    }
    
    // ============ 모든 부분 수열 확인 (홀수 길이만) ============
    // i: 부분 수열의 길이를 결정 (0, 2, 4, ... → 홀수 길이)
    for (int i=0;i<=N-1;i+=2){
        
        // j: 앞에서 제거할 개수 (왼쪽부터 0개, 1개, 2개, ...)
        for (int j=0;j<=i;j++){
            // ============ 부분 수열 범위 계산 ============
            // i: 부분 수열의 길이 - 1 (홀수: 1, 3, 5, ...)
            // j: 앞에서 제거할 개수
            // N-(i-j): 뒤에서 제거할 개수
            
            int size=N-(i-j)-j+1;  // 부분 수열의 길이
            // 예: N=6, i=2, j=0 → size=6-2-0+1=5
            // 예: N=6, i=2, j=1 → size=6-1-1+1=5
            
            int m=0;  // test_arr의 인덱스
            
            // ============ arr[j]부터 arr[N-(i-j)-1]까지 부분 수열 복사 ============
            for (int k=j;k<N-(i-j);k++){
                test_arr[m]+=arr[k];  // 부분 수열의 원소 저장
                m+=1;
            }
            
            // ============ 이 부분 수열의 중앙값이 B인지 확인 ============
            // distin 함수는 배열을 정렬하고 중앙값을 확인
            num+=distin(test_arr,size,B);  // 결과가 1이면 num에 1 더함
            
            // ============ test_arr 초기화 (다음 부분 수열을 위해) ============
            for (int k=0;k<N;k++){
                test_arr[k]=0;
            }
        }
    }
    
    // ============ 결과 출력 ============
    printf("중앙값이 %d인 부분수열은 : %d개 입니다.",B,num);
}